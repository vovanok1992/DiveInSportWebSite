jQuery(document).ready(function() {
    var offset = 220;
    var duration = 500;
    jQuery(window).scroll(function() {
        if (jQuery(this).scrollTop() > offset) {
            jQuery('.flog').fadeIn(duration);
        } else {
            jQuery('.flog').fadeOut(duration);
        }
    });
    
});